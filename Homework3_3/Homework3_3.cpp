#include <stdio.h>
//编写程序，输入任意10个整数，输出同时能被2和3整除的数
main()
{
	int i , a[10] ;  //注意定义数组是定义a[10]而非a[i]
	printf ("请输入10个整数并以空格间隔：");
	for ( i=0 ; i<10 ; i++ )  //注意for里用分号间隔
	{
		scanf ("%d" , &a[i] );
		if ( a[i]%2==0 && a[i]%3==0 )
		{
			printf ("其中能同时被2和3整除的数有%d\n" , a[i] );
		}
	}
}
